package arrays;

public class print {
	

	    
	    static int[] arr = {10, 20, 30, 40, 50};

	    public static void main(String[] args) {
	        System.out.println("Elements in the static array:");

	        for (int value : arr) {
	            System.out.print(value + " ");
	        }
	    }
	}


